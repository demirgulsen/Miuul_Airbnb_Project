# listing dataset;
#
# id : Airbnb'nin kayıt için benzersiz tanımlayıcısı
# listing_url : Listelemenin(evin) url bilgisi
# last_scraped : Bu listelemenin "kazındığı" tarih ve saat (UTC.)
# name : Listelemenin adı
# description : Listelemenin ayrıntılı açıklaması
# neighborhood_overview :Ev sahibinin mahalleye ilişkin açıklaması
# picture_url : Listeleme için Airbnb tarafından barındırılan normal boyutlu görselin URL'si
# host_id : Airbnb'nin ev sahibi/kullanıcı için benzersiz tanımlayıcısı
# host_url : Ev sahibinin Airbnb sayfası
# host_name : Ev sahibinin/ sahiblerinin adı
# host_since : Ev sahibinin/kullanıcının oluşturulduğu tarih. Airbnb misafiri olan ev sahipleri için bu, misafir olarak kayıt oldukları tarih olabilir.
# host_location : Ev sahibinin/kullanıcının kendi bildirdiği konum
# host_about : Ev sahibinin/sahiblerinin hakkında açıklama
# host_response_time :Ev sahibinin/sahiblerinin kullanıcılara yanıt süresi
# host_response_rate : Ev sahibinin/sahiblerinin yanıt oranı
# host_acceptance_rate : Ev sahibinin/sahiblerinin rezervasyon taleplerini kabul etme oranı.
# host_is_superhost : Ev sahibinin ne kadar süre ev sahibi olduğu ve beğenilme oranını belirtir (En iyi ev sahiplerinden mi bilgisi)
# host_thumbnail_url : Ev sahibinin sahip olduğu liste sayısı (Airbnb hesaplamalarına göre)
# host_picture_url : Ev sahibinin resim url bilgisi
# host_neighbourhood : Ev sahibinin mahalle bilgisi
# host_listings_count :Ev sahibinin sahip olduğu listeme sayısı(Veri setinin son tarihine kadar ne kadar kiralama yaptığının bilgisi) (Airbnb hesaplamalarına göre)
# host_total_listings_count : Ev sahibinin sahip olduğu toplam listeme(toplam ev kiralama) sayısı (Airbnb hesaplamalarına göre)
# host_verifications :Ev sahibinin iletişim bilgilerine sahip olup olmadığı
# host_has_profile_pic :Ev sahibinin profil fotoğrafı
# host_identity_verified :Ev sahibinin doğrulama id si
# neighbourhood : nahalle
# neighbourhood_cleansed : Açık veya kamuya açık dijital şekil dosyaları tarafından tanımlanan mahallelere karşı enlem ve boylam kullanılarak coğrafi olarak kodlanan mahalle.
# latitude : Evin enlem bilgisi
# longitude : Evin boylam bilgisi
# property_type : Kendi seçtiğiniz tesis türü. Oteller ve Oda ve Kahvaltılar bu alanda ev sahipleri tarafından bu şekilde tanımlanır
# room_type : Oda türü (Tüm ev/apt | Özel oda | Paylaşılan oda | Otel])
# accommodates : Listelemenin maksimum kapasitesi
# bathrooms :Evdeki banyo sayısı
# bathrooms_text :Listedeki banyo sayısı.
# Airbnb web sitesinde, banyolar alanı bir sayıdan metinsel bir açıklamaya dönüşmüştür. Daha eski çizimler için banyolar kullanılır
# bedrooms : Yatak odası sayısı
# beds : Yatak(lar)ın sayısı
# amenities : Olanaklar (Ev içinde sunulan imkanlar)
# price : Evin kiralanma fiyatı
# minimum_nights : minimum gece konaklama sayısı (takvim kuralları farklı olabilir)
# maximum_nights : Listeleme için maksimum gece konaklama sayısı (takvim kuralları farklı olabilir)
# minimum_minimum_nights : Takvimdeki en küçük minimum_gece değeri (365 gece sonrasına bakarak)
# maximum_minimum_nights : Takvimdeki en büyük minimum_gece değeri (365 gece sonrasına bakarak)
# minimum_maximum_nights : Takvimdeki en küçük maksimum_gece değeri (365 gece sonrasına bakarak)
# maximum_maximum_nights : Takvimdeki en büyük maximum_night değeri (365 gece sonrasına bakarak)
# minimum_nights_avg_ntm : takvimden alınan ortalama minimum_gece değeri (365 gece sonrasına bakarak)
# maximum_nights_avg_ntm : takvimden alınan ortalama maksimum_gece değeri (365 gece sonrasına bakarak)
# has_availability : Kiralama için aktif olup olmadığı bilgisi
# availability_30 : Listelemenin 30 gün sonraki kullanılabilirliği
# availability_60 : Listelemenin 60 gün sonraki kullanılabilirliği
# availability_90 : Listelemenin 90 gün sonraki kullanılabilirliği
# availability_365 : Listelemenin 360 gün sonraki kullanılabilirliği
# number_of_reviews : Listenin sahip olduğu yorum sayısı
# number_of_reviews_ltm : Listenin sahip olduğu son 12 ay içindeki yorum sayısı
# number_of_reviews_l30d :Listenin sahip olduğu 30 gün içindeki yorum sayısı
# first_review : İlk/en eski incelemenin(yorum almanın) tarihi
# last_review :  Son/en yeni incelemenin tarihi
# review_scores_rating : Değerlendirme puanı
# review_scores_accuracy : Doğruluk için inceleme puanı
# review_scores_cleanliness : Temi̇zli̇k için i̇nceleme puanı
# review_scores_checkin : Kontrol için inceleme puanı
# review_scores_communication : İletişim için i̇nceleme puanı
# review_scores_location : Konum için i̇nceleme puanı
# review_scores_value : Genel memnuniyet için i̇nceleme puanı
# license : Ruhsat/izin/kayıt numarası
# instant_bookable : Misafirin, ev sahibinin rezervasyon talebini kabul etmesine gerek kalmadan otomatik olarak rezervasyon yapıp yapamayacağı. Ticari bir listelemenin göstergesidir.
# calculated_host_listings_count : Ev sahibinin hesaplanan listeleme sayısı (Evini kaç kez kiraya verdiğinin bilgisi)
# calculated_host_listings_count_entire_homes :   Ev sahibinin şehir/bölge coğrafyasında geçerli kazıma işleminde sahip olduğu Tüm ev/apt listelerinin sayısı
# calculated_host_listings_count_private_rooms : Ev sahibinin geçerli kazıma işleminde şehir/bölge coğrafyasında sahip olduğu Özel oda listelerinin sayısı
# calculated_host_listings_count_shared_rooms :  Ev sahibinin geçerli kazıma işleminde şehir/bölge coğrafyasında sahip olduğu Paylaşılan oda listelerinin sayısı
# reviews_per_month : Listelemenin kullanım ömrü boyunca sahip olduğu inceleme sayısı(aylık aldığı yorum sayısı)
#
